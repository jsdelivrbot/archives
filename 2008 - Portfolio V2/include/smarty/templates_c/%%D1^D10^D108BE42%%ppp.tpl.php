<p>
Le <acronym title="Projet Personnel et Professionnel">PPP </acronym> est une réflexion personnelle visant à mettre l'étudiant en accord avec ses souhaits professionnels immédiats et futurs, ses aspirations 
personnelles ainsi que ses capacités, afin de concevoir un parcours de formation cohérent avec le ou les métiers choisis.
</p>

<p style="margin-bottom:0">
  Ma réflexion s'est ainsi penchée sur <strong>les métiers du web</strong>, les principaux (à mon sens) étant :
</p>

<ul>
<li>- <span>Développeur web indépendant</span></li>
<li>- <span>Développeur multimédia</span></li>
<li>- <span>Intégrateur</span></li>
<li>- <span>Spécialiste en référencement</span></li>
</ul>
   
<p>
  A l'heure où j'écris ces lignes, il est incontestable que ces métiers, et principalement le métier de développeur freelance 
  m'attirent considérablement. 
</p>
<p>
  Cependant, au cours de mon DUT, je me suis rendu compte que la programmation software était elle aussi attrayante. 
  De surcroit, un des avantages du software par rapport au web est que le côté artistique devient secondaire. Or, le manque d'inspiration 
  artistique est une de mes faiblesses.<br /><br />
</p>


